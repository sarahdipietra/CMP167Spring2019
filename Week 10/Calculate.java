public class Calculate {
	
	public static int Multiply(int a, int b) {
		return a* b;
	}
	
	public static double Multiply(double a, double b) {
		return a * b;
	}
	
	public static double getRectangleArea(double width, double length) {
		return width *length;
	}
}
